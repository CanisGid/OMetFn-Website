<h2>Governance details</h2>
<p>We expect to create community-run working groups, supported by the OMetFn team.</p>
<p><a title="Board of Directors" href="/board/#nicolas">Nicolas BALDECK</a> (Founder and President) manage OMetFn overall in terms of general management and strategic direction, and report to the <a title="Board of Directors" href="/board">Board of Directors</a>, who are responsible for the financial and legal probity of the Open Meteo Foundation.</p>
<p>We plan to have a distinguished Advisory Board for providing specialist expertise and guidance.</p>
