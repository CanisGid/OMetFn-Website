<h1>Get Involved</h1>

<p>Our projects are open, and we encourage you to join.</p>
<p>Whatever your background is, our projects are often multidisciplinary, and so your skills will be useful.</p>

<p>Everyone can contribute to science. We should get inspiration from past projects such as <a href="http://setiathome.berkeley.edu/" target="_blank">SETI@Home</a> (where your screensaver was seeking for extra-terrestrial life) or <a href="http://fold.it" target="_blank">fold.it</a>, (where <a href="http://www.huffingtonpost.com/2011/09/19/aids-protein-decoded-gamers_n_970113.html" target="_blank">gamers easily solved an AIDS problem researchers were fighting for 15 years</a>).</p>

<h2>Participate to an existing project</h2>

<p>On every project website, you will find detailed instructions about how to contribute.</p>

<p>See the list of our activities on our <a href="/projects">Projects page</a>.</p>


<p>We are working hard for enabling you to contribute more. However, the Open Meteo Foundation is very young, and we may still not had sufficient time for explaining all the ways you can get involved. In any case, do not hesitate to <a href="/contact">contact us</a>.</p>

<h2 id="project-proposal">Start your own project</h2>

<p>Do you get an existing project or a new idea you want to explore ?</p>

<p>We can help you develop your project, bringing ressources such as computing power, hosting, archiving. You will also benefit from our social network.</p>

<p>We accept projects following those simple criterias :</p>
<ul>
  <li>Open Data and Content: data and content, or any other kind of resources, produced by the project will be openly licensed in a way that is compatible with the Open Definition.</li>
  <li>Free/Open Source Software: software created by the project will be openly licensed (i.e. in conformance with the OSI Open Source Definition)</li>
  <li>Public process and politeness: the project will be run in a public way and encourage participation from outsiders. Interaction both within the project and the wider community will be governed by basic principles of tolerance, respect and politeness.</li>
</ul>

<p>To discuss your project with us, send an email to getinvolved[a]ometfn.org .</p>

