<h1>Impliquez-vous</h1>

<p>Nos projets sont ouverts et nous vous invitons à contribuer.</p>
<p>Quelque soit votre formation, nos projets sont souvent multidisciplinaires vos compétences pourront donc &ecirc;tre utiles.</p>

<p>Tout le monde peut contribuer à la science. Nous devrions nous inspirer des autres projets tels que <a href="http://setiathome.berkeley.edu/" target="_blank">SETI@Home</a> (où votre écran de veille recherche la vie extraterrestre) ou <a href="http://fold.it" target="_blank">fold.it</a>, (ou des <a href="http://www.huffingtonpost.com/2011/09/19/aids-protein-decoded-gamers_n_970113.html" target="_blank">joueurs ont facilement résolu un problème sur lequel la recherche contre le SIDA buttait depuis 15 ans.</a>).</p>

<h2>Participez à un projet existant</h2>

<p>Sur les pages de tous nos projets, vous trouverez des instructions détaillées sur comment contribuer.</p>

<p>Référez-vous à notre <a href="/projects">liste de projets</a>.</p>

<p>Nous travaillons dur pour permettre au plus grand nombre de contribuer à nos projets. Cela dit, notre association est encore jeune et nous n'avons pas encore eu le temps tout détailler. Dans tous les cas, n'hésitez pas à <a href="/contact">nous contacter</a>.

<h2 id="project-proposal">Montez votre propre projet</h2>

<p>Vous avez projet existant ou une idée que vous aimeriez faire vivre ?</p>

<p>Nous pouvons vous aider à développer votre projet, en vous apportant des ressources telles que de la puissance de calcul, de l'hébergement web ou des capacités d'archivage. Vous bénéficierez aussi de notre réseau social.</p>

<p>Nous acceptons uniquement les projets conformes à ces critères :</p>
<ul>
  <li>Données et contenus ouverts : les données, le contenu ou tout autre ressource produite par le projet devront être mis sous une licence compatible avec l'Open Definition.</li>
  <li>Logiciel libre/Open-source: les logiciels produits devront être placés sous une licence libre, en accord avec l'OSI Open Source Definition.</li>
  <li>Discussions publiques et courtoises : le projet devra être animé d'une manière publique et encourager l'implication de la communauté. Les interactions entre le projet et la communauté devront se faire selon les règles basiques de courtoisie, de tolérance et de respect.</li>
</ul>

<p>Pour discuter de votre projet avec nous, envoyez un email à : getinvolved[a]ometfn.org .</p>

